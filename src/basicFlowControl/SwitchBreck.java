package basicFlowControl;

public class SwitchBreck {
	public static void main(String[] args) {
		int a = 10; 
		switch (a) {
		case 10:
			System.out.println("ratan");
			
		case 20:
			System.out.println("aun");
			

		case 30:
			System.out.println("durga");
			break;
		default:
			System.out.println("default");
			break;
		}
	}

}
